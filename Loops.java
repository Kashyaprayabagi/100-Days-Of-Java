public class Loops {
    public static void main(String args[]) {
        //using for loops
        for(int i = 0; i < 5; i++) {
            System.out.println("100 Days Of Java!!!");
        }

        //using while loop
        int i = 0;
        while(i < 10) {
            System.out.println("100 Days Of Java!!!");
            i++;
        }
    }
}
